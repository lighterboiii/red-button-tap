/**
 * Случайный дроп вещи под редкость тапа.
 * @param {Rarity} rarity
 * @returns {{ slot: GearSlot, label: string }}
 */
export function rollDrop(rarity) {
  const slot = GEAR_SLOTS[Math.floor(Math.random() * GEAR_SLOTS.length)];
  const pool = POOLS[rarity]?.[slot] ?? POOLS.common[slot];
  const label = pool[Math.floor(Math.random() * pool.length)];
  return { slot, label };
}
